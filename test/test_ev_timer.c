void test_timer_cb(void *args)
{
    dbg_always("timer expired!!");
}

int test_ev_timer()
{
    unsigned char i = 5;
    for(; i < 10; ++ i)
    {
        ev_timer_t *timer = ev_timer_create(i*1000, test_timer_cb, NULL);
        ev_timer_start(timer);
    }

    sleep(11);
}